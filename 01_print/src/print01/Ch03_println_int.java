package print01;

public class Ch03_println_int {

	public static void main(String[] args) {
		// 문자열일 때 +, - 로 연산하고 싶으면 반드시 괄호를 넣어야 함
		
		// 덧셈
		System.out.println("30 + 20 = " + (30 + 20));
		// 뺄셈
		System.out.println("30 - 20 = " + (30 - 20));
		// 곱셈
		System.out.println("30 * 20 = " + 30 * 20);
		// 나눗셈
		System.out.println("30 / 20 = " + 30 / 3);
	}

}
